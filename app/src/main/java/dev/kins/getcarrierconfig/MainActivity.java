    String GetString(String str)
    {
        return "\"" + str.replaceAll("\"", "\\\\\"") + "\"";
    }

    String GetNumber(String value)
    {
        return value;
    }
    public String getDeviceName() {
        String manufacturer = Build.MANUFACTURER;
        String model = Build.MODEL;
        if (model.toLowerCase().startsWith(manufacturer.toLowerCase())) {
            return capitalize(model);
        } else {
            return capitalize(manufacturer) + " " + model;
        }
    }


    private String capitalize(String s) {
        if (s == null || s.length() == 0) {
            return "";
        }
        char first = s.charAt(0);
        if (Character.isUpperCase(first)) {
            return s;
        } else {
            return Character.toUpperCase(first) + s.substring(1);
        }
    }
    String GetArray(Object[] arr)
    {
        String value="[";
        String prefix="";
        for (int i = 0; i < arr.length; i++) {
            value = value + prefix + GetValue(arr[i]);
            prefix=", ";
        }
        return value + "]";
    }

    String GetValue(Object o){
        if(o.getClass().isArray()){
            return GetArray((Object[]) o);
        }
        else if(o instanceof String) {return GetString(o.toString());}
        else if(o instanceof Boolean) {return GetNumber(o.toString());}
        else if(o instanceof Long) {return GetNumber(o.toString());}
        else if(o instanceof Integer) {return GetNumber(o.toString());}
        else if(o instanceof Double) {return GetNumber(o.toString());}
        else if(o instanceof Float) {return GetNumber(o.toString());}
        return GetString(o.toString());
    }

    public void OnClick(View view)
    {
        try{
            Context context = getApplicationContext();
            CarrierConfigManager ccm = context.getSystemService(CarrierConfigManager.class);
            PersistableBundle carrierConfig = ccm.getConfigForSubId(1);
            String data="{\n  \"Device\": \"" + getDeviceName() + "\"";
            String prefix=",\n  ";
            for (int i = 0; i < keys.length; i++) {
                try {
                    Object value = carrierConfig.get(keys[i]);
                    data = data + prefix + "\"" + keys[i] + "\" : " + GetValue(value);
                }catch (Exception ex)
                {
                    Log.v("GetCarrierConfig", "Couldn't get data for: " + keys[i]);
                }
            }
            data = data + "\n}";
            Log.v("GetCarrierConfig", data);
            Intent sendIntent = new Intent();
            sendIntent.setAction(Intent.ACTION_SEND);
            sendIntent.putExtra(Intent.EXTRA_TEXT, data);
            sendIntent.setType("text/plain");

            Intent shareIntent = Intent.createChooser(sendIntent, "Carrier Config Data");
            startActivity(shareIntent);
        }catch (Exception ex)
        {
            Snackbar snackbar
                    = Snackbar.make(
                        findViewById(android.R.id.content),
                            "Make sure app has phone read permission",
                            Snackbar.LENGTH_LONG);
            snackbar.show();
        }
    }

